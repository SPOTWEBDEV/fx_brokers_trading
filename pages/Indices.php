  stock index, while some have several. These baskets of individual stocks are often ranked by
  independent institutions, like major banks or specialist companies like the FTSE Group or the
  Deutsche Börse. They also come in different sizes. For instance, the FTSE 100 tracks the share
  price of the top 100 companies listed on the London Stock Exchange, in terms of market
  capitalisation. The ASX 200 tracks the share price of the 200 top companies listed on the
  Australian Securities Exchange (ASX), while the SPI 200 futures contract is a benchmark equity
  index futures contract, based on the ASX 200 index. </p>
