  <div class="row">
  <div class="col-sm-6">
  <div class="text_block pad-for-what-com-sec">
  <p class="trade-indices-sec-subhead-para-align">Stock market indices give the measure of a specific
  stock market. They represent the value of a group of stocks from a country, and represents the
  overall, current and historic performance of a specific set of stocks. The calculated value of
  the stock index is used by investors as an indicator of the current value of their component
  stocks. Investors can find out the expected returns over time by comparing the current and
  historic index levels. </p>
  </div>
  <div class="hidden-xs">
  <img class="margin-image-top" src="../static/globalcityindex-live/files/index-trading.png" alt="index-trading" title="" style="margin-top: 20px;max-width: 100%!important;">
